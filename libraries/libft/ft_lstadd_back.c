#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	last = *lst;
	if (*lst == NULL)
	{
		*lst = new;
	}
	else
	{
		ft_lstlast(last);
		last->next = new;
	}
}
